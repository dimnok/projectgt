-- ===================================================================
-- Миграция: Удаление legacy колонки role_id из таблицы profiles
-- ===================================================================
-- ОПИСАНИЕ:
-- Колонка role_id в таблице profiles является legacy полем и не используется
-- в бизнес-логике. Роли теперь хранятся в company_members.role_id.
-- 
-- БЕЗОПАСНОСТЬ:
-- - Колонка не используется в коде (перезаписывается данными из company_members)
-- - Нет индексов на эту колонку
-- - Нет внешних ключей, ссылающихся на эту колонку
-- - Нет RLS политик, использующих эту колонку
-- ===================================================================

BEGIN;

-- Удаляем колонку role_id из profiles, если она существует
DO $$ 
BEGIN
    IF EXISTS (
        SELECT 1 
        FROM INFORMATION_SCHEMA.COLUMNS 
        WHERE table_schema = 'public' 
        AND table_name = 'profiles' 
        AND column_name = 'role_id'
    ) THEN
        ALTER TABLE public.profiles DROP COLUMN role_id;
        RAISE NOTICE 'Колонка role_id успешно удалена из таблицы profiles';
    ELSE
        RAISE NOTICE 'Колонка role_id не существует в таблице profiles, пропускаем удаление';
    END IF;
END $$;

COMMIT;

-- ===================================================================
-- Результат:
-- ✅ Legacy колонка role_id удалена из profiles
-- ✅ Роли теперь хранятся только в company_members.role_id
-- ✅ Безопасно и не влияет на работу приложения
-- ===================================================================
