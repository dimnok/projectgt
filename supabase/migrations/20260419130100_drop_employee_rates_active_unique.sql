-- ЭТАП E3 (продолжение): удаляем устаревший частичный уникальный индекс
-- idx_employee_rates_active_unique.
--
-- Причины:
--   1. Полностью покрывается новым EXCLUDE-constraint employee_rates_no_overlap
--      (см. миграцию 20260419130000), который защищает от ЛЮБЫХ пересечений
--      периодов, а не только от двух открытых ставок.
--   2. Старый индекс не учитывает company_id, что в мультикомпанийном
--      сценарии будет ложно блокировать допустимые вставки.
--   3. Индекс перехватывает нарушения раньше EXCLUDE, искажая диагностику
--      (unique_violation вместо exclusion_violation).

DROP INDEX IF EXISTS public.idx_employee_rates_active_unique;
