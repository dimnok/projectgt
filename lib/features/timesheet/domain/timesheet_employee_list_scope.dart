/// Режим фильтрации списка сотрудников в сетке табеля по сумме часов за период.
///
/// Применяется **только в UI** (сетка на экране). Excel-экспорт этот сегмент
/// не использует — см. [visibleTimesheetGridEmployees].
enum TimesheetEmployeeListScope {
  /// Базовый список (фильтр по объектам и правила для уволенных).
  all,

  /// Только сотрудники с суммой часов в периоде больше нуля.
  withHours,

  /// Только сотрудники с нулевой суммой часов в периоде (в том числе без строк).
  withoutHours,
}
