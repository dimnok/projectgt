/// Абстрактный источник данных для календаря смен.
///
/// Отвечает за получение агрегированных данных смен по датам.
/// Отделён от выгрузки для независимой реализации.
abstract class ShiftsDataSource {
  /// Получает агрегированные данные смен за месяц.
  ///
  /// Возвращает список смен с суммами по датам текущего месяца.
  /// Используется для отображения в календаре.
  Future<List<Map<String, dynamic>>> getShiftsForMonth(DateTime month);

  /// Получает детали смен за конкретный день.
  ///
  /// [date] - день, за который нужны детали.
  /// Возвращает список смен за этот день с разбивкой по объектам и системам.
  Future<Map<String, dynamic>> getShiftsForDate(DateTime date);
}
